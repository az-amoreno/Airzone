numero = bin(10) # 10 en binario
print(numero)

numero = int(numero,2)  # Convierte un número (indicándole su base a la derecha) a decimal
print(numero)

numero = hex(10) # 10 en hexadecimal
print(numero)

numero = int(numero,16)
print(numero)

numero = abs(-8) # Valor ABSoluto del número (distancia del número al 0)
print(numero)

numero = round(5.6) # Redondea
print(numero)

numero = round(5.5) # Redondea
print(numero)

numero = round(5.4999999) # Redondea
print(numero)